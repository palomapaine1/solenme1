import pandas as pd
import requests
import streamlit as st

# Funci칩n para obtener datos de una API
def obtener_datos_api(api_url):
    response = requests.get(api_url)
    if response.status_code == 200:
        data = response.json()
        return pd.DataFrame(data)
    else:
        st.error(f"Error al obtener los datos de la API: {response.status_code}")
        return None
# P치gina de interacci칩n con los datos
def pagina_interaccion():
    api_url = "https://restcountries.com/v3.1/all"
    df = obtener_datos_api(api_url)
    
    if df is not None:
        st.title("Interacci칩n con los Datos")
        st.write(df.head())

     
